def replace_steps(content, recipe_id, new_steps):
    id_pos = content.find(f'"id": "{recipe_id}"')
    if id_pos == -1:
        print(f"NOT FOUND: {recipe_id}")
        return content
    steps_start = content.find('"steps": [', id_pos)
    if steps_start == -1:
        print(f"steps not found for {recipe_id}")
        return content
    depth = 0
    pos = steps_start + len('"steps": ')
    while pos < len(content):
        if content[pos] == '[':
            depth += 1
        elif content[pos] == ']':
            depth -= 1
            if depth == 0:
                end = pos
                break
        pos += 1
    old = content[steps_start:end + 1]
    # Build replacement
    lines = ['        "steps": [']
    for s in new_steps:
        escaped = s.replace('\\', '\\\\').replace('"', '\\"').replace('\n', '\\n')
        lines.append(f'            "{escaped}",')
    lines.append('        ]')
    new_block = '\n'.join(lines)
    return content.replace(old, new_block, 1)


