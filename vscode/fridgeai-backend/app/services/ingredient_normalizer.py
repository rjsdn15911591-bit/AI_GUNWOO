import re

# 완전한 동의어만 포함 (같은 재료, 다른 이름)
# 비슷한 재료(대파/쪽파, 청양고추/풋고추 등)는 구분 유지
SYNONYM_MAP: dict[str, str] = {
    # 계란 = 달걀 (완전 동의어)
    "달걀": "계란",
    # 소고기 = 쇠고기 (완전 동의어)
    "소고기": "쇠고기",
    "우육": "쇠고기",
    # 돼지 (축약형)
    "돼지": "돼지고기",
    # 게맛살 = 맛살
    "게맛살": "맛살",
    "크래미": "맛살",
    # 치킨 관련 (완제품 치킨 ↔ 생닭 구분 유지)
    "후라이드치킨": "치킨",
    "후라이드 치킨": "치킨",
    "양념치킨": "치킨",
    "양념 치킨": "치킨",
    "프라이드치킨": "치킨",
}

# "냉동 X" → "X" 로 정규화할 완제품 목록
_FROZEN_PREFIX_STRIP = re.compile(
    r'^냉동\s*(피자|만두|치킨|떡볶이|탕수육|돈까스|파스타|햄버거|핫도그|순대|김밥|도시락|볶음밥|짜장면|짬뽕)$'
)


def normalize_name(name: str) -> str:
    """재료명 정규화: 공백 제거 → 냉동 접두사 제거 → 동의어 통일"""
    cleaned = name.strip()
    # "냉동 피자" → "피자" 등 완제품 접두사 제거
    m = _FROZEN_PREFIX_STRIP.match(cleaned)
    if m:
        cleaned = m.group(1)
    return SYNONYM_MAP.get(cleaned, cleaned)
