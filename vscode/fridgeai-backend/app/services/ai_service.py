def parse_ai_response(content: str) -> list[dict]:
    try:
        data = json.loads(content)
        if isinstance(data, dict):
            data = data.get("items", data.get("ingredients", []))
        if not isinstance(data, list):
            return []
        return [
            item
            for item in data
            if isinstance(item, dict) and item.get("confidence", 0) >= 0.3
        ]
    except (json.JSONDecodeError, TypeError):
        return []


async def analyze_image(image_bytes: bytes, content_type: str = "image/jpeg") -> list[dict]:
    b64 = base64.b64encode(image_bytes).decode()
    client = get_openai_client()
    response = await client.chat.completions.create(
        model=settings.OPENAI_MODEL,
        max_tokens=4096,
        messages=[
            {"role": "system", "content": SYSTEM_PROMPT},
            {
                "role": "user",
                "content": [
                    {
                        "type": "image_url",
                        "image_url": {"url": f"data:{content_type};base64,{b64}", "detail": "high"},
                    },
                    {"type": "text", "text": "이미지를 격자 스캔하여 모든 내용물을 빠짐없이 분석하세요. 특히 술(위스키·와인·소주·맥주·칵테일RTD 등)과 음료(주스·탄산음료 등)를 최우선으로 인식하고 각각 개별 항목으로 기재하세요. 캔·병의 라벨·색상·모양으로 주류 종류를 구분하세요. 가려진 물체, 부분만 보이는 물체도 포함하고, 채소·과일·캔·소스도 각각 개별 재료명으로 분리하세요."},
                ],
            },
        ],
        response_format={"type": "json_object"},
    )
    raw_content = response.choices[0].message.content
    return parse_ai_response(raw_content)
